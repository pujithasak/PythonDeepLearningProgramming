# Question: Take two numbers from user and perform arithmetic operations on them.
print("Performing Arithmetic Operations on Two Numbers:")
# Asking user to enter inputs for performing Arithmetic operations and convering the inputs into integer type
num1 = int(input('Enter the first number:'))
num2 = int(input('Enter the second number:'))

# Here we are printing the outputs of arithmetic Operations
print('Addition:',num1 + num2)
print('Subtraction:',num1-num2)
print('Multiplication:',num1 * num2)
print('Division:',num1 / num2)
print('Modulus:',num1 % num2)
print('Exponent:',num1 ** num2)